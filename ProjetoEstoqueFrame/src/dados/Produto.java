package dados;

/**
 *
 * @author Gordramos
 */
public class Produto {
    
    private int codigoProduto;
    private String nomeProduto;
    private int quantidadeProduto;
    private float valorProduto;
    private String localProduto;

    /**
     * @return the codigoProduto
     */
    public int getCodigoProduto()
      {
        return codigoProduto;
      }

    /**
     * @param codigoProduto the codigoProduto to set
     */
    public void setCodigoProduto(int codigoProduto)
      {
        this.codigoProduto = codigoProduto;
      }

    /**
     * @return the nomeProduto
     */
    public String getNomeProduto()
      {
        return nomeProduto;
      }

    /**
     * @param nomeProduto the nomeProduto to set
     */
    public void setNomeProduto(String nomeProduto)
      {
        this.nomeProduto = nomeProduto;
      }

    /**
     * @return the quantidadeProduto
     */
    public int getQuantidadeProduto()
      {
        return quantidadeProduto;
      }

    /**
     * @param quantidadeProduto the quantidadeProduto to set
     */
    public void setQuantidadeProduto(int quantidadeProduto)
      {
        this.quantidadeProduto = quantidadeProduto;
      }

    /**
     * @return the valorProduto
     */
    public float getValorProduto()
      {
        return valorProduto;
      }

    /**
     * @param valorProduto the valorProduto to set
     */
    public void setValorProduto(float valorProduto)
      {
        this.valorProduto = valorProduto;
      }

    /**
     * @return the localProduto
     */
    public String getLocalProduto()
      {
        return localProduto;
      }

    /**
     * @param localProduto the localProduto to set
     */
    public void setLocalProduto(String localProduto)
      {
        this.localProduto = localProduto;
      }
    
    


}
